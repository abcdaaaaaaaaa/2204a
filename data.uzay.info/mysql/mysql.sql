CREATE TABLE SensorData (
    id INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    Alcohol VARCHAR(10),
    Benzene VARCHAR(10),
    Hexane VARCHAR(10),
    CH4 VARCHAR(10),
    Smoke VARCHAR(10),
    CO2 VARCHAR(10),
    Toluen VARCHAR(10),
    NH4 VARCHAR(10),
    Aceton VARCHAR(10),
    LPG VARCHAR(10),
    Propane VARCHAR(10),
    CO VARCHAR(10),
    H2 VARCHAR(10),
    Air VARCHAR(10),
    Avg1 VARCHAR(10),
    sdCPM VARCHAR(10),
    Arrayval VARCHAR(10),
    SpaceData100 VARCHAR(10),
    SpaceData200 VARCHAR(10),
    MQ2  VARCHAR(10),
    MQ3 VARCHAR(10),
    MQ4 VARCHAR(10),
    MQ135 VARCHAR(10),
    lat VARCHAR(12),
    lng VARCHAR(12),
    reading_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
)
