    Dist0 VARCHAR(10),
    Dist30 VARCHAR(10),
    Dist60 VARCHAR(10),
    Dist90 VARCHAR(10),
    Dist120 VARCHAR(10),
    Dist150 VARCHAR(10),
    Dist180 VARCHAR(10),
    Average VARCHAR(10),
    Temp0 VARCHAR(10),
    Temp30 VARCHAR(10),
    Temp60 VARCHAR(10),
    Temp90 VARCHAR(10),
    Temp120 VARCHAR(10),
    Temp150 VARCHAR(10),
    Temp180 VARCHAR(10),
    Flux0 VARCHAR(10),
    Flux30 VARCHAR(10),
    Flux60 VARCHAR(10),
    Flux90 VARCHAR(10),
    Flux120 VARCHAR(10),
    Flux150 VARCHAR(10),
    Flux180 VARCHAR(10),
    rgb0 VARCHAR(20),
    rgb30 VARCHAR(20),
    rgb60 VARCHAR(20),
    rgb90 VARCHAR(20),
    rgb120 VARCHAR(20),
